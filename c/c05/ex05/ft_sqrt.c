#include <unistd.h>

int		ft_sqrt(int nb)
{
	int num;

	num = 1;
	if (nb == 1)
		return (1);
	while (num <= nb / 2 && num <= 46340)
	{
		if (num * num == nb)
			return (num);
		num++;
	}
	return (0);
}
