#include <unistd.h>

int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int count;

	count = 0;
	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			count = s1[i] - s2[i];
			return (count);
		}
		i++;
	}
	return (count);
}
